package com.example.heralert;

public class SelfDefense {
    String nameSelfDefense;
    int imageSelfDefense;

    public SelfDefense(String nameSelfDefense, int imageSelfDefense) {
        this.nameSelfDefense = nameSelfDefense;
        this.imageSelfDefense = imageSelfDefense;
    }

    public String getNameSelfDefense() {
        return nameSelfDefense;
    }

    public void setNameSelfDefense(String nameSelfDefense) {
        this.nameSelfDefense = nameSelfDefense;
    }

    public int getImageSelfDefense() {
        return imageSelfDefense;
    }

    public void setImageSelfDefense(int imageSelfDefense) {
        this.imageSelfDefense = imageSelfDefense;
    }
}
